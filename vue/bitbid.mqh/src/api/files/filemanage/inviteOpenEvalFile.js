import ajax from '@/common/js/ajax'

export const inviteOpenEvalFile = {
  /**
   * 单条查询-邀请招标开评定标信息
   * @param {*} query [查询信息]
   */
  getInviteFile (query) {
    return ajax({
      url: '/inviteOpenEvalFile-infos',
      method: 'get',
      params: query
    })
  },
  /**
   * 新增接口-邀请招标开评定标信息
   * @param {*} data [主体数据]
   */
  saveInviteFile (data) {
    return ajax({
      url: '/inviteOpenEvalFile-infos',
      method: 'post',
      data: data
    })
  }
}
