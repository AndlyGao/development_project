package com.shenyuan.militarynews.beans.data;

public class LocalStateBean {

	String id;
	String lasttime;
	String read_state;
	int favorite_state;
	int like_state;
	String data_item;
	String data_item_article;
	private int tread_state = 0;
	String collect_time;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLasttime() {
		return lasttime;
	}

	public void setLasttime(String lasttime) {
		this.lasttime = lasttime;
	}

	public String getRead_state() {
		return read_state;
	}

	public void setRead_state(String read_state) {
		this.read_state = read_state;
	}

	public int getFavorite_state() {
		return favorite_state;
	}

	public void setFavorite_state(int favorite_state) {
		this.favorite_state = favorite_state;
	}

	public int getLike_state() {
		return like_state;
	}

	public void setLike_state(int like_state) {
		this.like_state = like_state;
	}

	public String getData_item() {
		return data_item;
	}

	public void setData_item(String data_item) {
		this.data_item = data_item;
	}

	public String getData_item_article() {
		return data_item_article;
	}

	public void setData_item_article(String data_item_article) {
		this.data_item_article = data_item_article;
	}

	public int getTread_state() {
		return tread_state;
	}

	public void setTread_state(int tread_state) {
		this.tread_state = tread_state;
	}

	public String getCollect_time() {
		return collect_time;
	}

	public void setCollect_time(String collect_time) {
		this.collect_time = collect_time;
	}
	
}
