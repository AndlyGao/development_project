public class FhVideo {
    private String title;
    private long length;
    private String imageUrl;
//    public static final String MOCK_PLAY_URL = "http://59.49.82.1/video19.ifeng.com/video09/2017/08/05/5339383-280-068-140753.mp4";
    private String videoUrl;

    public FhVideo(String title, long length, String imageUrl, String videoUrl) {
        this.title = title;
        this.length = length;
        this.imageUrl = imageUrl;
        this.videoUrl = videoUrl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public long getLength() {
        return length;
    }

    public void setLength(long length) {
        this.length = length;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getVideoUrl() {
        return videoUrl;
    }

    public void setVideoUrl(String videoUrl) {
        this.videoUrl = videoUrl;
    }
}
